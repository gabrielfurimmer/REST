package com.webservice.rest.VO;

public class VOAuto {
	private String auto;
	private String color;
	private boolean colorPermitido;
	public String getAuto() {
		return auto;
	}
	public void setAuto(String auto) {
		this.auto = auto;
	}
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	public boolean isColorPermitido() {
		return colorPermitido;
	}
	public void setColorPermitido(boolean colorPermitido) {
		this.colorPermitido = colorPermitido;
	}

	
}
